Cube::~Cube() {
	glDeleteVertexArrays(1, &m_VAO);
	glDeleteBuffers(1, &m_VBO);
}

void Cube::draw() const {
	glBindVertexArray(m_VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}

void Cube::bindTexture() const {
	m_texture.bind(GL_TEXTURE0);
}

void Cube::load() {


	glGenVertexArrays(1, &m_VAO);
	glBindVertexArray(m_VAO);

	glGenBuffers(1, &m_VBO);
	glBindBuffer(GL_ARRAY_BUFFER, m_VBO);
	glBufferData(GL_ARRAY_BUFFER, (m_vertices.size() * sizeof(float)), m_vertices.data(), GL_STATIC_DRAW);

	// LINKING ATTRIBUTES OF THE BUFFERS
	// Vertex coordinates attributes
	// location, #values per vertex, type of values, normalizing values, stride between vertices attributes, offset since the buffer first position
	glEnableVertexAttribArray(0);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);

	// Texture coordinates attributes
	glEnableVertexAttribArray(1);
	glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));

	m_texture = Texture(GL_TEXTURE_2D, "resources/container.jpg");

	// Release OpenGL bindings/handles
	glBindVertexArray(0);
	glDisableVertexAttribArray(0);
	glDisableVertexAttribArray(1);
	glBindBuffer(GL_ARRAY_BUFFER, 0);
}
